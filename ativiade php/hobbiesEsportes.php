<p>1. Meditação
Tem dificuldades de se concentrar e anda muito estressado? Saiba que a meditação pode te ajudar a resolver esses problemas!
</p>
<br>
<p>2. Corrida
A corrida é um esporte que promove o desejo de superação, além de proporcionar muitos benefícios para a saúde.
</p>
<br>
<p>3. Academia
Realizar treinos na academia de forma regular é um ótimo método de autocuidado, já que promove a saúde física, contribui para o alívio do estresse e melhora o humor.
</p>
<br>
<p>4. Yoga
O yoga é uma atividade que busca trabalhar de forma conjunta o corpo e a mente. É uma ótima opção de hobby, pois aumenta a disposição e ajuda a aliviar possíveis dores do dia a dia!</p>
<br>
<p>5. Dança
A dança é outra maneira de exteriorizar os sentimentos e as aulas permitem ainda trabalhar o fortalecimento muscular, estimular o cérebro e, claro, se divertir!</p>
