<p>1. Skate
O skate é um esporte que faz com que o praticante exercite diversas partes do corpo. Dessa forma, a flexibilidade, a maior resistência física e o fortalecimento das articulações são benefícios que o skatista consegue adquirir praticando esse hobby.</p>
<br>
<p>2. Passeios culturais
Os passeios culturais são ótimos para pessoas que procuram por passatempos cheios de conhecimento e curiosidade. Geralmente, eles resgatam uma parte da história e permitem que o visitante volte no tempo.</p>
<br>
<p>3. Jogos de tabuleiro
Não são só as crianças que podem se divertir com os jogos de tabuleiro. Existem até versões específicas para adultos e esse passatempo traz muitos benefícios para o desenvolvimento social e cognitivo.</p>
<br>
<p>4. Viajar
Viajar é uma excelente maneira de entrar em contato com outras culturas e relaxar. Listar os lugares que quer visitar e ir conhecendo-os sempre que possível é um bom modo de garantir momentos de descanso e lazer!</p>
<br>
<p>5. Culinária
A culinária é outro hobby que possibilita colocar a criatividade em prática. Procurar receitas diferentes ou de pratos que goste muito pode ser um ótimo jeito de iniciar a atividade.</p>
