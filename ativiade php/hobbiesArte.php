<p>1. Pintura
A pintura é uma ótima forma de se expressar e é possível investir tanto em aulas como praticá-la de forma mais intuitiva.</p>
<br>
<p>2. Aulas de teatro
A interpretação é uma ótima maneira de exercitar as habilidades de comunicação, desenvolver a consciência corporal e aumentar a criatividade.</p>
<br>
<p>3. Dança
A dança é outra maneira de exteriorizar os sentimentos e as aulas permitem ainda trabalhar o fortalecimento muscular, estimular o cérebro e, claro, se divertir!</p>
<br>
<p>4. Fotografia
A fotografia permite registrar situações, pessoas e objetos, captando sua essência. Exercitá-la permite descobrir nos ângulos e modos de ver o mundo!
</p>
<br>
<p>5. Escrita
Escrever é um passatempo que permite um maior desenvolvimento de funções criativas. Desde a manutenção de diários até anotações de sonhos, tudo vale.</p>
